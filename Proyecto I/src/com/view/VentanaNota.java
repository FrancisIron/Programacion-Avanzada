    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTable1.getSelectedRow() != -1) {
            int mul = jComboBox1.getSelectedIndex() * 5;
            double nota = Double.parseDouble(JOptionPane.showInputDialog(null, "Ingrese nota a añadir", "Ingresar nota"));
            String descripcion = JOptionPane.showInputDialog(null, "Ingrese el motivo de la nota", "Ingresar descripción");
            a.modificarNota(jTable1.getSelectedRow() + mul, nota, descripcion);
            pasarAlumno();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione una nota", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

