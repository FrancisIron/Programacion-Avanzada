    public void pasarAlumno() {
        String[] fila = new String[]{"Nota", "Contenido"};
        String[][] columna = new String[5][2];
        int mult = jComboBox1.getSelectedIndex() * 5;
        int cont = 0;
        for (int i = mult; i < mult + 5; i++) {
            columna[cont][0] = "" + a.getCalificaciones().get(i).getNota();
            columna[cont][1] = a.getCalificaciones().get(i).getContenido();
            cont++;

        }
        jTable1.setModel(new DefaultTableModel(columna, fila));

    }

